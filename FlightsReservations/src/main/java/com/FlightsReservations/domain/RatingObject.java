public class RatingObject {
	private Long id;
	private float score;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public float getScore() {
		return score;
	}
	public void setScore(float score) {
		this.score = score;
	}
	public RatingObject() {
		super();
	}
	
	
}
